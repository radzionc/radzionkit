export const S_IN_MIN = 60;
export const H_IN_DAY = 24;
export const D_IN_WEEK = 7;
export const S_IN_HOUR = S_IN_MIN * S_IN_MIN;
export const S_IN_DAY = H_IN_DAY * S_IN_HOUR;
export const H_IN_WEEK = H_IN_DAY * D_IN_WEEK;
export const MIN_IN_HOUR = 60;
export const MIN_IN_DAY = H_IN_DAY * MIN_IN_HOUR;

export const MS_IN_SEC = 1000;
export const MS_IN_MIN = S_IN_MIN * MS_IN_SEC;
export const MS_IN_HOUR = S_IN_MIN * MS_IN_MIN;
export const MS_IN_DAY = H_IN_DAY * MS_IN_HOUR;
export const MS_IN_WEEK = 7 * MS_IN_DAY;
